let age;

if (0 <= age <= 1) {
    console.log('infant')
} else if (1 < age <= 3) {
    console.log('toddler')
} else if (3 < age <= 10) {
    console.log('child')
} else if (10 < age <= 12) {
    console.log('preteen')
} else if (12 < age <= 17) {
    console.log('toddler')
} else if (17 < age  <= 20) {
    console.log('young adult')
}

